import turtle
turtle.bgcolor("black")
t = turtle.Turtle()
t.fillcolor("dark red")
t.begin_fill()
t.circle(100)
t.end_fill()
t.pensize(4)
t.penup()
t.goto(40, 110)
t.pendown()
t.fillcolor("gray")
t.begin_fill()
t.circle(20)
t.end_fill()
t.penup()
t.goto(-40, 110)
t.pendown()
t.begin_fill()
t.circle(20)
t.end_fill()
t.penup()
t.goto(40, 110)
t.pendown()
t.fillcolor("white")
t.begin_fill()
t.circle(20)


t.penup()
t.goto(-10, 50)
t.pendown()
t.setheading(90)

t.pensize(3)
t.color("black")

t.circle(-20, -180)

turtle.done()
